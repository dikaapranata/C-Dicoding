#include <stdio.h>
#include <stdlib.h>

int main()
{
    /* Kamus */
    int X=5;
    int Y=8;

    /* Program */
    printf("Hasil X < Y : %d \n", X < Y);
    printf("Hasil X > Y : %d \n", X > Y);
    printf("Hasil X <= Y : %d \n", X <= Y);
    printf("Hasil X >= Y : %d \n", X >= Y);
    printf("Hasil X == Y : %d \n", X == Y);
    printf("Hasil X != Y : %d \n", X != Y);
    printf("Hasil X == X : %d \n", X == X);
    printf("Hasil X != X : %d \n", X != X);



    return 0;
}
